int main()
{
    int test;
    int n,x1,y1,z1,x2,y2,z2;
    int x,y,z,a,b,c,cas=0;
    cin>>test;
    while(test--)
    {
        cin>>n;
        x=y=z=0;a=b=c=100000;
        while(n--)
        {
            cin>>x1>>y1>>z1>>x2>>y2>>z2;
            x=max(x,x1);
            y=max(y,y1);
            z=max(z,z1);
            a=min(a,x2);
            b=min(b,y2);
            c=min(c,z2);
        }
        int ans=0;
        if(a-x<0||b-y<0||c-z<0) ans=0;
        else
        {
            ans=(a-x)*(b-y)*(c-z);
        }
        printf("Case %d: %d\n",++cas,ans);
    }
    return 0;
}

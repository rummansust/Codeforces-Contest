int mat[1000];
char str[100500];
int col[60];

int sum[60][100050];

int main()
{
    #ifdef MAHDI
//    Read;
//    Write;
    #endif // MAHDI
    for(char i='a';i<='z';i++) mat[i]=(int) (i-'a');
    for(char i='A';i<='Z';i++) mat[i]=(int) (i-'A'+26);
    int n;
    sf("%d",&n);
    sf("%s",str);
    int tot=0;
    vector<int> cr;
    for(int i=0;i<n;i++)
    {
        if(!col[mat[str[i]]])
        {
            tot++;
            cr.psb(mat[str[i]]);
            col[mat[str[i]]]=1;
        }
        sum[mat[str[i]]][i+1]++;
    }
    for(int i=0;i<60;i++)
    {
        for(int j=1;j<=n;j++)
        {
            sum[i][j]+=sum[i][j-1];
        }
    }
    int res=n;
    for(int i=1;i<=n;i++)
    {
        int lo=1,hi=n,mid,tp,cres=n;
        while(lo<=hi)
        {
            mid=(lo+hi)>>1;
            tp=0;
            for(int j=0;j<tot;j++)
            {
//                if(i==6)
//                deb(mid,cr[j],sum[cr[j]][i]-sum[cr[j]][mid-1]);
                if(sum[cr[j]][i]-sum[cr[j]][mid-1]<=0) break;
                tp++;
            }
            if(tp==tot)
            {
                cres=min(i-mid+1,cres);
                lo=mid+1;
            }
            else hi=mid-1;
        }
        res=min(res,cres);
    }
    pf("%d\n",res);
    return 0;
}















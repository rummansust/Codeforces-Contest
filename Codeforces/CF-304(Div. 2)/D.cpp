const int MAX = 5100000;

#define SZ1 5100000

bool sieve[SZ1+10];
int val[SZ1+10];

ll sum[MAX];

void _sieve()
{
    int i,j,k,r;
    for(int i=0;i<SZ1;i++) val[i]=i;
    int lim=(int)sqrt(SZ1)+1;
    for(i=2; i<lim; i+=1+(i>2))
    {
        if(!sieve[i])
        {
            for(int j=i;j<SZ1;j+=i)
            {
                sieve[j]=true;
                while(val[j]%i==0)
                {
                    val[j]/=i;
                    sum[j]++;
                }
            }
        }
    }
    for(int j=2;j<SZ1;j++)
    {
        sum[j]+=(val[j]>1)+sum[j-1];
    }
    return;
}


int dp[1001][101][2];
int vp[1001][101][2];
int loop=1;
int pr10[1001];
int n,k;
int MOD;

int rec(int pos,int M,int tz)
{
    if(pos==0)
    {
        return (!tz && !M);
    }
    int &ret = dp[pos][M][tz];
    int &vet = vp[pos][M][tz];
    if(vet==loop) return ret;
    vet=loop;
    ret=0;
    int nM,ntz,idx=n-pos;
    for(int i=0+(pos==1);i<10;i++)
    {
        nM=(M+(pr10[idx]*i))%k;
        if(!tz && !M) nM=0;
        ntz=!(i || !tz);
        ret=(ret+rec(pos-1,nM,ntz))%MOD;
    }
    return ret;
}

int main()
{
    SF("%d %d %d",&n,&k,&MOD);
    pr10[0]=1;
    for(int i=1;i<=n;i++) pr10[i]=(pr10[i-1]*10)%k;
    PF("%d\n",rec(n,0,1));
    return 0;
}












